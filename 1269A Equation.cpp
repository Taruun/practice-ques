// Composite numbers are those that can be divided evenly by numbers other than 1 and themselves. 
// If we take two composite numbers and divide them by the given input, the result will also be a composite number, and the difference between these two divided composite numbers will be equal to the given input


#include <iostream>
using namespace std;

int main() {
    int n; cin >> n;
    cout << 9*n << " " << 8*n << '\n';
}